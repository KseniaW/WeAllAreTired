package com.class07;

public class Task {

	public static void main(String[] args) {

		int a = 1;
		while (a <= 100) {
			System.out.print(a + " ");
			a++;
		}
		System.out.println();

		int b = 100;
		while (b >= 1) {
			System.out.print(b + " ");
			b--;
		}
		System.out.println();

		int c = 20;
		while (c <= 100) {
			System.out.print(c + " ");
			c += 2;
		}
	}

}
