package groupProject;

public class task5 {

	public static void main(String[] args) {
		 System.out.println("Before swapping");  
		  int x = 10;  
		  int y = 20;  
		  System.out.println("x:" + x);  
		  System.out.println("y:" + y);  
		 
		  System.out.println();
		  System.out.println("After swapping");  
		  x = x + y;  //30
		  y = x - y;  //10
		  x = x - y;  //20
		  System.out.println("x:" + x);  
		  System.out.println("y:" + y);  
		 }  

	}


